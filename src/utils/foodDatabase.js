// Helper function to search food database
export const searchFoodDatabase = (query) => {
  const allFoods = Object.values(FOOD_DATABASE).flat();
  const searchTerm = query.toLowerCase();
  
  return allFoods.filter(food => 
    food.name.toLowerCase().includes(searchTerm) ||
    food.category.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results
};

// Helper function to get foods by category
export const getFoodsByCategory = (category) => {
  return FOOD_DATABASE[category] || [];
};

// Helper function to get random food suggestions
export const getRandomFoodSuggestions = (count = 5) => {
  const allFoods = Object.values(FOOD_DATABASE).flat();
  const shuffled = allFoods.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
